                <div style="font-weight:bold">6. Postupak u slučaju nezgode/štete</div>


                <p style="text-align: justify;">
                    U slučaju nastupanja štete ili saobraćajne nesreće u kojoj je učestvovaloo zakupljeno
                    vozilo,
                    zakupac je dužan da odmah o tome telefonom obavesti zakupodavca i policiju, i spreči
                    daljnja
                    oštećenja.
                    Zakupac mora zakupodavcu dostaviti potpun izveštaj i priložiti skicu
                    nezgode. Izveštaj o nezgodi mora sadržati imena i adrese učesnika u nezgodi i eventualne
                    svedoke,
                    kao i registarske oznake vozila koja su sudelovala u nezgodi.
                    Zakupac ne sme davati nikakve izjave u vezi sa štetom na vozilu. Otuđenje vozila kao i
                    štete
                    od
                    požara, naleta divljači, i sl., moraju se neodložno prijaviti zakupodavcu i policiji.
                </p>
                <br>

                <div style="font-weight:bold">7. Osiguranje</div>


                <p style="text-align: justify;">


                    Vozilo koje se zakupljuje je kasko osigurano. Eventualno učešće u šteti Zakupca,
                    zavisi
                    od
                    uslova
                    koje je Zakupac dogovorio sa Zakupodavcem i to u pisanoj formi.
                </p>
                <br>
                <div style="font-weight:bold">8. Odgovornost zakupca</div>
                <p style="text-align: justify;">

                    Zakupac je neograničeno odgovoran za sve štete, obaveze i potraživanja, koje nastanu
                    prilikom
                    upotrebe vozila od strane zakupca ili osobe koja je vozilo koristila uz saglasnost
                    zakupca
                    mogle
                    nastati zakupodavcu, kao vlasniku vozila ili trećim licima, uključujući njegovu
                    ličnu
                    krivicu,
                    krivicu trećih lica, slučaj ili višu silu. Zakupac snosi i sve troškove koji nastanu
                    usled
                    neadekvatnog korišćenja i korišećenja vozila koje ne pokriva kasko osiguranje.
                    Zakupac je obavezan da vozilo vrati zakupodavcu u istom stanju u kom ga je i
                    preuzeo, u
                    suprotnom je
                    dužan zakupodavcu nadoknaditi sve troškove i štete povezane sa tim vozilom.
                    Zakupac nema pravo da zadrži vozilo, bez obzira iz kog razloga i odnosa i po kom
                    osnovu
                    može
                    poticati njegovo potraživanje, kao ni da da svoja eventualna potraživanja prema
                    zakupodavcu
                    prebija
                    sa potraživanjima zakupodavca iz ovog ugovora.
                    Zakupac je dužan da strogo poštuje servisni interval proizvođača vozila.
                    Nepoštovanje
                    istih
                    prouzrokuje gubitak garancije na vozilu. Zakupac je dužan da odmah i bez odlaganja
                    obavesti
                    poslovnicu izjamljivanja da je na komandnom displeju ili bord kompjuteru u
                    iznajmljenom
                    vozilu
                    počela da gori BILO KOJA upozoravajuća lampica, radi intervencije na vozilu. Ukoliko
                    se
                    Zakupac
                    ogluši na ovo upozorenje i ne javi se u poslovnicu iznajmljivanja vozila, snosiće
                    pun
                    penal
                    radi
                    gubitka garancije proizvođača izjamljenog vozila, koji čine sastavni deo Opštih
                    uslova.

                </p>
                <br>
                <div style="font-weight:bold">9. Odgovornost zakupodavca</div>
                <p style="text-align: justify;">

                    Zakupodavac nije odgovoran za štetu koja bi zakupcu ili trećim licima mogla nastati
                    usled
                    upotrebe
                    vozila.

                </p>
                <br>
                <div style="font-weight:bold">10. Izmene i dopune ugovora/dostava</div>
                <p style="text-align: justify;">

                    Sve izmene i dopune ovog ugovora moraju biti u pismenoj formi. Dostavljanje se vrši
                    isključivo
                    preporučenom poštom

                </p>
                <br>
                <div style="font-weight:bold"> 11. Nadležan sud</div>
                <p style="text-align: justify;">

                    Stvarno i mesno je nadležan sud u mestu sedišta zakupodavca.
                </p>
                <br>
                <div style="font-weight:bold">12. Završne odredbe</div>
                <p style="text-align: justify;">


                    Ovaj ugovor je zaključen u prostorijama zakupodavca u dva (2) istovetna primerka,
                    od
                    kojih
                    svaka
                    strana zadražava po jedan (1) primerak.
                </p>



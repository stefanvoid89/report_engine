                <div style="font-weight:bold">1. Trajanje zakupa/preuzimanje i povraćaj vozila</div>
                <p style="text-align: justify;"> Zakup počinje da teče preuzimanjem vozila, koje je
                    predmet
                    ovog ugovora (u daljem tekstu
                    vozilo).
                    Najkraće trajanje zakupa je 24 časa (1 dan). Vremensko prekoračenje ugovorenog
                    trajanja
                    zakupa
                    za
                    više od 4 časa obračunava se kao puni dan zakupa. Vraćanjem vozila pre isteka
                    ugovorenog
                    trajanja
                    zakupa, zakupac stiče pravo na smanjenje ugovorene zakupnine, osim za slučaj
                    započetog
                    ugovorenog
                    dana.
                    Preuzimanje vozila u zakup i povraćaj vozila zakupodavcu, obavljaju se
                    isključivo u
                    servisu
                    zakupodavca, i tada se utvrđuje stanje i eventualna oštećenja na i u vozilu.
                    Zakupodavac
                    predaje
                    vozilo punog rezervoara i zakupac je dužan da vrati vozilo punog rezervoara.
                    Zakupac je
                    obavezan
                    da
                    vozilo vrati u toku radnog vremena zakupodavca, najkasnije u ugovorenom vremenu
                    za
                    povraćaj
                    vozila.
                    U suprotnom, zakupodavac ima pravo da sam dođe u posed svog vozila i prateće
                    dokumentacije,
                    a
                    zakupac se odriče prava na državinsku zaštitu. </p>
                <br>

                <div style="font-weight:bold">2. Zakupnina i način plaćanja</div>
                <p>
                    Ugovorena zakupnina se odnosi na korišćenje vozila, njegovo održavanje,
                    osiguranje i
                    registraciju,
                    kao i sve pripadajuće obaveze. Troškovi u vezi upotrebe vozila kao što su
                    benzin/dizel,
                    ulje,
                    antifriz i druga sredstva, koje je za vreme trajanja zakupa potrebno dopunit kao
                    i
                    naknada
                    putarine,
                    parkinga, garažan isu uključeni u zakupninu i snosi ih zakupac. Zakupac snosi i
                    sve
                    troškove
                    koji
                    nastanu usled neadekvatnog korišćenja vozila, oštećenja na vozilu, nepoštovanja
                    saobraćajnih
                    i
                    propisa o parkiranju vozila.
                    Dnevna zakupnina se odnosi na period od 24 časa zakupa vozila. Zakupac je dužan
                    podmiriti
                    ukupnu
                    zakupninu i sve ostale eventualne troškove najkasnije pri povraćaju vozila u
                    visini
                    utvrđenoj
                    važećim cenovnikom zakupodavca. Na zahtev zakupodavca. zakupac je obavezan da
                    pre
                    preuzimanja
                    vozila
                    položi traženi iznos avansa.</p>
                <br>

                <div style="font-weight:bold">3. Korišćenje i upravljanje vozilom</div>
                <p style="text-align: justify;">
                    Vozilom sme da upravlja isključivo zakupac, odnosno ako je zakupac pravno lice,
                    tada
                    samo
                    lice,
                    koje
                    je u tu svrhu pisanim putem ovlastio zakupac i koje je kao takvo navedeno u ovom
                    ugovoru
                    i
                    to
                    samo
                    dok poseduje važeće ovlašćenje za upravljanje vozilom. Preduslov za upravljanje
                    vozilom
                    je
                    posedovanje važeće vozačke dozvole.
                    Kada vozilo nije u funkciji vrata i prozori moraju biti zatvoreni odnosno
                    zaključani.
                </p>
                <br>

                <div style="font-weight:bold">4. Zabrana korišćenja </div>

                <p style="text-align: justify;">
                    Zakupcu nije dozvoljeno korišćenje vozila za dole navedene radnje:

                    <div style="margin-left:20px;font-size:11px"> a) učestvovanje na trkama, vuča drugih
                        vozila i
                        sl.</div>
                    <div style="margin-left:20px;font-size:11px"> b) prevoz lako zapaljivih, otrovnih ili
                        drugih
                        opasnih
                        materija ili prevoz osoba
                        korišćenje,
                        kao i korišćenje vozila za obuku</div>
                    <div style="margin-left:20px;font-size:11px"> c) dalje iznajmljivanje ili posuđivanje
                        trećim
                        licima,
                        kao i neovlašćeno
                        davanje ključa trećim licima</div>
                    <div style="margin-left:20px;font-size:11px"> d) vožnja van teritorije Republike Srbije
                        bez
                        prethodne
                        pismene
                        saglasnosti zakupodavca</div>
                    <div style="margin-left:20px;font-size:11px"> e) drugu upotrebu, koja izlazi iz okvira
                        ugovora
                    </div>
                    <div style="margin-left:20px;font-size:11px"> f) zabranjeno je pušenje u vozilu</div>
                </p>
                <br>
                <div style="font-weight:bold">5. Popravke</div>

                <p style="text-align: justify;">
                    Popravke potrebne za uredan rad ili saobraćajnu sigurnost vozila, obavlja sam
                    zakupodavac.
                    Za
                    popravke u drugim servisima, zakupac treba da obezbedi prethodnu pismenu
                    saglasnost
                    zakupodavca.
                    Ukoliko zakupac nije lično odgovoran za kvar, troškove popravke snosi
                    zakupodavac.
                    Zakupcu
                    nije
                    dozvoljeno da samovoljno da izdaje naloge za popravak vozila.
                    Ako zakupac za vreme korišćenja zakupa bude sprečen da koristi vozilo zbog
                    popravke,
                    kvara
                    ili
                    oštećenja na vozilu, koje nije sam prouzrokovao, troškovi zakupa vozila se za
                    vreme
                    popravke
                    vozila
                    ne obračunavaju.</p>


